-- 1. Login to PDB from CDB
ALTER SESSION SET "_ORACLE_SCRIPT"=true;

-- 2. Create User
CREATE USER EXPERIMENT
    IDENTIFIED BY EXPERIMENT
    DEFAULT TABLESPACE TBL_SPC_EXPERIMENT 
    QUOTA UNLIMITED ON TBL_SPC_EXPERIMENT
;

-- 3. Grant 'CREATE SESSION' system privilege
GRANT CREATE SESSION TO EXPERIMENT WITH ADMIN OPTION;

-- 4. Grant Other Privileges
GRANT
      CREATE ANY MATERIALIZED VIEW
    , ALTER ANY MATERIALIZED VIEW
    , DROP ANY MATERIALIZED VIEW
    , QUERY REWRITE
    , GLOBAL QUERY REWRITE
TO 
    EXPERIMENT
WITH 
    ADMIN OPTION
;

GRANT
    CREATE TABLE
TO
    EXPERIMENT
;

-- 5. Check User
SHOW USER;


GRANT SELECT TO EXPERIMENT;